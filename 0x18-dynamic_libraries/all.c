#include <stdio.h>

/**
*add - add two int
* @a: first int
* @b: second int
* 
* Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - sub two int
* @a: first int
* @b: second int
* 
* Return: subtraction
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - mul two int
* @a: first int
* @b: second int
* 
* Return: multiply
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divide two int
* @a: first int
* @b: second int
* 
* Return: division
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod two int
* @a: first int
* @b: second int
* 
* Return: mod
*/
int mod(int a, int b)
{
	return (a % b);
}
